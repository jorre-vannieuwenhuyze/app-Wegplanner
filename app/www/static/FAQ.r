FAQitem("Hoe komen jullie tot de kleurkeuze?","
Onze kleurkeuze wijkt af van de Vlaamse richtlijnen maar overlapt sterk met Nederland. 
In Nederland wordt vaak gebruik gemaakt van een bruinrode ondergrond om woonzones te markeren, maar ook om steeds de plaats van de fietser op de weg aan te duiden.
Hierbij wordt geen onderscheid gemaakt tussen aparte fietspaden of fietssuggestiestroken.
Deze kleur heeft dan ook geen bijkomende regelgevende betekenis; het dwingt bijvoorbeeld geen voorrang af. 
Naast het bruinrood worden er geen speciale kleuren gebruikt. 
In onze plannen vertrekken we eveneens van deze eenvoud.
Hiermee gaan we in tegen de Vlaamse richtlijnen die aanraadt om fietssuggestiestroken, in tegenstelling tot bruinrode fietspaden, in oker of andere kleuren aan te duiden. 
Dit leidt enkel tot meer inconsistentie en verwarring onder de weggebruikers. 
Wij pleiten dus voor een consistent gebruik van de kleur bruinrood om de plaats van fietsers aan te duiden.
Het verschil tussen fietspaden en -suggestiestroken wordt volgens ons beter aangeduide met duidelijke witte belijning, drempels en boordstenen.
