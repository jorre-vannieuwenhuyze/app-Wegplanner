FAQitem("Waarom gebruiken jullie asfalt in de ene plannen en betonstraatstenen in de andere?","
Het verschil tussen asfalt en betonstraatstenen (of klinkers) duidt in onze plannen consistent op het verschil tussen 
verbindings- en ontsluitingswegen (asfalt) en lokale toegangswegen/woonstraten (betonstraatstenen), 
en dit zowel voor gemotoriseerd verkeer als voor fietsers. 
Hierdoor wordt elke weggebruiker er constant aan herinnerd in welk type straat of fietspad zij of hij rijdt. 
Als er betonstraatstenen liggen, dan weet de weggebruiker dat je deze straat enkel moet gebruiken om op een bestemming in de buurt te geraken
en dat je je snelheid moet minderen. Deze straten hebben dan ook steeds een snelheidsregime van maximum 30km/u.
Als er asfalt ligt, dan weet de weggebruiker dat je deze straat kan gebruiken voor langere verplaatsingen.
Dit principe wordt heel vaak toegepast in Nederland.
