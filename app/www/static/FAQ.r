FAQitem("In de Vlaamse wegcategorisering worden fietswegen als lokale wegen type III beschouwd. Waarom wijken jullie daarvan af?","
Net zoals bij gemotoriseerd verkeer geloven we dat fietsverkeerstromen op termijn (lees, als steeds meer mensen gaan fietsen) beter in goede banen worden geleid.
Het is immers niet wenselijk dat toegangswegen (zoals woonstraten) evolueren tot drukke fietsverbindingswegen, zeker met de intrede van speedpedelecs.
Hier wordt dus best al grondig over nagedacht. 
Wij kiezen er alvast voor om dezelfde terminologie en gelijkaardige infrastructuur te gebruiken als voor gemotoriseerd verkeer.
We maken dus ook een onderschied tussen verbidings- en ontsluitingswegen voor fietsers en lokale toegangswegen. 
Dit maakt onze wegen opnieuw een pak eenvoudiger en leesbaarder voor de weggebruikers.
")
,
FAQitem("Waarom maken jullie geen onderscheid tussen ontsluitings- en verbindingswegen?","
Ontsluitings- en verbindingswegen hebben een gelijkaardige functie: Ze moeten gemotoriseerd verkeer zo vlot mogelijk laten doorstromen. 
Dit staat in contrast met lokale toegangswegen (bv. woonstraten) waar doorstroming niet prioritair is, maar wel de toegang tot verschillende terreinen. 
Daarom kiezen we ervoor om zo veel mogelijk een visueel onderscheid te maken tussen ontsluitings- en verbindingswegen enerzijds en lokale toegangswegen anderzijds.
Het enige verschil tussen ontsluitings- en verbindingswegen is dat verbindinswegen een continue vlotte route zouden moeten garanderen tussen verschillende urbane kernen,
terwijl ontsluitingswegen vanuit de verbindinswegen zouden moeten doodlopen in die urbane kernen.
Voor fietsverkeer passen we dezelfde logica toe zoals je kan lezen onder de vorige vraag.
")
,
FAQitem("Waar horen speedpedelecs thuis?","
Speedpedelecs worden vaak gezien als fietsers maar halen bijna dezelfde snelheid als gemotoriseerd verkeer in bebouwde kom. 
Gewone fietspaden van 1m5 tot 1m75 zijn bovendien te smal voor speedpedelecs om comfortabel minder snelle fietsers in te halen.
Dit leidt steeds vaker tot frustraties onder fietsers en gebruikers van speedpedelecs.
Daarom ijveren we voor de aanleg van bredere fietspaden op plaatsen waar speedpedelecs op het fietspad horen en  
voor een snelheidslimiet van 30km/u op smallere fietspaden en paden gedeeld met wandelaars.
")
,
