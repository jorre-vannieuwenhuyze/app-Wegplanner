FAQitem("Waarom zijn er verbindingswegen aan 30km/u?","
Verbindingswegen dienen om verbindingen te maken tussen urbane gebieden. 
In principe zou je er als weggebruiker aan een redelijke snelheid over moeten kunnen rijden.
Jammer genoeg dragen we hier in Vlaanderen de last van een chaotische ruimtelijke ordening met ons mee.
Anders dan in andere landen, bouwden we en bouwen we nu nog steeds onze verbindingswegen vol, wat leidt tot de fameuze lintbebouwing.
Op sommige plaatsen staan er zelfs scholen en andere voorzieningen voor kinderen en de jeugd langs onze drukke verbindingswegen.
Die fouten oplossen kan enkel op een heel lange termijn met een duidelijk beleid rond ruimtelijke ordening.
Daarom voorzien we nu plannen voor verbindingswegen waar slechts 30km/u mag gereden worden, bijvoorbeeld in de buurt van scholen.
Op heel lange termijn zijn deze plannen hopelijk overbodig. 
")
